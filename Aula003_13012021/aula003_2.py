# Operadores Básicos
a = 10
b = 20

print(f'soma:{a+b}') #soma
print(f'subtração: {a-b}') #subtração
print(f'multiplicação: {a*b}') #multiplicação
print(f'divisão: {a/b}') #divisão
print(f'módulo: {a%b}') #módulo ou resto
print(f'potenciação: {a**b}') #potenciação
print(f'divisão float: {float(a)//float(b)}') #divisão float
